const BASE_URL = 'https://swapi.dev/api';
const MAX_RETRIES = 2;
const RETRY_DELAY = 1000;
const ITEMS_PER_PAGE = 10;

const createMockResponse = (
  searchTerm: string = '',
  page: number = 1
): APIResponse => {
  const filteredCharacters = searchTerm
    ? MOCK_CHARACTERS.filter((char) =>
        char.name.toLowerCase().includes(searchTerm.toLowerCase())
      )
    : MOCK_CHARACTERS;

  // Simulate pagination for mock data
  const startIndex = (page - 1) * ITEMS_PER_PAGE;
  const endIndex = startIndex + ITEMS_PER_PAGE;
  const paginatedResults = filteredCharacters.slice(startIndex, endIndex);

  const totalPages = Math.ceil(filteredCharacters.length / ITEMS_PER_PAGE);

  return {
    count: filteredCharacters.length,
    next: page < totalPages ? `${BASE_URL}/people/?page=${page + 1}` : null,
    previous: page > 1 ? `${BASE_URL}/people/?page=${page - 1}` : null,
    results: paginatedResults,
  };
};

const delay = (ms: number) => new Promise((resolve) => setTimeout(resolve, ms));

export const createPaginationInfo = (
  response: APIResponse,
  currentPage: number
): PaginationInfo => {
  const totalPages = Math.ceil(response.count / ITEMS_PER_PAGE);

  return {
    currentPage,
    totalPages,
    totalCount: response.count,
    hasNext: response.next !== null,
    hasPrevious: response.previous !== null,
  };
};

export class APIService {
  static async searchCharacters(
    searchTerm: string = '',
    page: number = 1,
    retryCount = 0
  ): Promise<APIResponse> {
    try {
      let url = `${BASE_URL}/people/`;
      const params = new URLSearchParams();

      if (searchTerm) {
        params.append('search', searchTerm);
      }

      params.append('page', page.toString());

      if (params.toString()) {
        url += `?${params.toString()}`;
      }

      const controller = new AbortController();
      const timeoutId = setTimeout(() => controller.abort(), 10000);

      const response = await fetch(url, {
        method: 'GET',
        headers: {
          Accept: 'application/json',
          'Content-Type': 'application/json',
        },
        signal: controller.signal,
      });

      clearTimeout(timeoutId);

      if (!response.ok) {
        if (response.status >= 400 && response.status < 500) {
          throw new Error(
            `Client error (${response.status}): ${response.statusText}. Please check your request and try again.`
          );
        }

        if (response.status >= 500) {
          throw new Error(
            `Server error (${response.status}): ${response.statusText}. The service is temporarily unavailable.`
          );
        }

        throw new Error(
          `Request failed with status ${response.status}: ${response.statusText}`
        );
      }

      const data = await response.json();
      return data;
    } catch (error) {
      if (error instanceof Error) {
        if (error.name === 'AbortError') {
          if (retryCount < MAX_RETRIES) {
            console.log(
              `Request timed out, retrying... (${retryCount + 1}/${MAX_RETRIES})`
            );
            await delay(RETRY_DELAY * (retryCount + 1));
            return this.searchCharacters(searchTerm, page, retryCount + 1);
          }
          console.warn(
            'Request timed out after multiple attempts. Using enhanced mock data as fallback.'
          );
          return createMockResponse(searchTerm, page);
        }

        if (
          error.message.includes('Failed to fetch') ||
          error.message.includes('NetworkError')
        ) {
          if (retryCount < MAX_RETRIES) {
            console.log(
              `Network error, retrying... (${retryCount + 1}/${MAX_RETRIES})`
            );
            await delay(RETRY_DELAY * (retryCount + 1));
            return this.searchCharacters(searchTerm, page, retryCount + 1);
          }
          console.warn(
            'Network error: Unable to connect to the Star Wars API. Using enhanced mock data as fallback.'
          );
          return createMockResponse(searchTerm, page);
        }

        if (error.message.includes('CORS')) {
          console.warn(
            'CORS error: Unable to access the Star Wars API. Using enhanced mock data as fallback.'
          );
          return createMockResponse(searchTerm, page);
        }

        throw error;
      }

      throw new Error('An unexpected error occurred while fetching data.');
    }
  }
}
